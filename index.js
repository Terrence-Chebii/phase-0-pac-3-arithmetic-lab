function add(a , b){
    return a + b
}
function subtract(a , b){
    return a - b
}
function multiply(a , b){
    return a * b
}
function divide(a , b){
    return a / b
}

function increment(n){
    return ++n
}

function decrement(n){
    return --n
}

function makeInt(n){
    parseInt("0" ,10);
    return n
}

function preserveDecimal(n){
   parseFloat("2.222", 10) 
   return n
}
